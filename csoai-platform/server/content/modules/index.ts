/**
 * Get quiz questions for a specific course and module
 * @param courseId - The course ID (1-15)
 * @param moduleIndex - The module index (0-based)
 * @returns Quiz questions array or undefined if not found
 */
export function getModuleQuiz(courseId: number, moduleIndex: number) {
  switch (courseId) {
    case 1: // EU AI Act
      return euAiActModules[moduleIndex];
    case 2: // NIST AI RMF
      return nistAiRmfModules[moduleIndex];
    case 3: // ISO 42001
      return iso42001Modules[moduleIndex];
    case 4: // TC260
      return tc260Modules[moduleIndex];
    case 5: // Maternal Covenant
      return maternalCovenantModules[moduleIndex];
    case 6: // SOAI-PDCA
      return soaiPdcaModules[moduleIndex];
    case 7: // AI Safety Fundamentals
      return aiSafetyFundamentalsModules[moduleIndex];
    case 8: // Ethics & Bias Detection
      return ethicsBiasDetectionModules[moduleIndex];
    case 9: // Regulatory Compliance Overview
      return regulatoryComplianceModules[moduleIndex];
    case 10: // UK AI Regulation
      return ukAiRegulationModules[moduleIndex];
    case 11: // Canada AIDA
      return canadaAidaModules[moduleIndex];
    case 12: // Australia AI Ethics
      return australiaAiEthicsModules[moduleIndex];
    case 13: // Healthcare AI Safety
      return healthcareAiSafetyModules[moduleIndex];
    case 14: // Financial Trading AI
      return financialTradingAiModules[moduleIndex];
    case 15: // Autonomous Vehicles
      return autonomousVehiclesModules[moduleIndex];
    default:
      return undefined;
  }
}

/**
 * Get the total number of modules for a course
 * @param courseId - The course ID (1-15)
 * @returns Number of modules or 0 if course not found
 */
export function getModuleCount(courseId: number): number {
  switch (courseId) {
    case 1: return euAiActModules.length;
    case 2: return nistAiRmfModules.length;
    case 3: return iso42001Modules.length;
    case 4: return tc260Modules.length;
    case 5: return maternalCovenantModules.length;
    case 6: return soaiPdcaModules.length;
    case 7: return aiSafetyFundamentalsModules.length;
    case 8: return ethicsBiasDetectionModules.length;
    case 9: return regulatoryComplianceModules.length;
    case 10: return ukAiRegulationModules.length;
    case 11: return canadaAidaModules.length;
    case 12: return australiaAiEthicsModules.length;
    case 13: return healthcareAiSafetyModules.length;
    case 14: return financialTradingAiModules.length;
    case 15: return autonomousVehiclesModules.length;
    default: return 0;
  }
}
